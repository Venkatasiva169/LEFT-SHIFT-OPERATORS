public class Leftshiftoperators {
        public static void main(String[] args) {
            int num1 = 4;  
            int num2 = -4; 
    
            
            int result1 = num1 << 2;  
            System.out.println("Result of " + num1 + " << 2 = " + result1);
    
            
            int result2 = num2 << 2;  
            System.out.println("Result of " + num2 + " << 2 = " + result2);
        }
    }
    

